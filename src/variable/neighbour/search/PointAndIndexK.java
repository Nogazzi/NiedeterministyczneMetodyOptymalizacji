public class PointAndIndexK {
    private Point point;
    private int indexK;
    public PointAndIndexK(Point point, int indexK){
        this.point = point;
        this.indexK = indexK;
    }

    public Point getPoint() {
        return point;
    }

    public int getIndexK() {
        return indexK;
    }
}
