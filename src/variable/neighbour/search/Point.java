public class Point {
    private double x1;
    private double x2;
    public Point(double x1, double x2){
        this.x1 = x1;
        this.x2 = x2;
    }
    public double getX1(){
        return this.x1;
    }
    public double getX2(){
        return this.x2;
    }
}
